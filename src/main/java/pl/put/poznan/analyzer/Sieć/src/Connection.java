public class Connection {
    private int from;
    private int to;
    private int value;

        public void setFrom(int from) {
            this.from = from;
        }

        public void setTo(int to) {
            this.to = to;
        }

        public void setValue(int value) {
            this.value = value;
        }

        public int getFrom() {
            return from;
        }

        public int getValue() {
            return value;
        }

        public int getTo() {
            return to;
        }
}
